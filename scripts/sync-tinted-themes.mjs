import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, "..")
const manifestPath = path.join(repoRoot, "vendor", "tinted-themes", "manifest.json")
const vendorDir = path.join(repoRoot, "vendor", "tinted-themes", "base16")
const outputPath = path.join(repoRoot, "src", "lib", "generated-tinted-themes.ts")
const sourceBaseUrl =
  "https://raw.githubusercontent.com/tinted-theming/schemes/spec-0.11/base16"

const manifest = JSON.parse(await fs.readFile(manifestPath, "utf8"))

await fs.mkdir(vendorDir, { recursive: true })

for (const entry of await fs.readdir(vendorDir)) {
  await fs.unlink(path.join(vendorDir, entry))
}

const schemes = []
for (const filename of manifest) {
  const response = await fetch(`${sourceBaseUrl}/${filename}`)
  if (!response.ok) {
    throw new Error(`Failed to download ${filename}: ${response.status} ${response.statusText}`)
  }

  const yaml = await response.text()
  await fs.writeFile(path.join(vendorDir, filename), yaml)
  schemes.push(parseBase16Yaml(filename, yaml))
}

schemes.sort((a, b) => a.name.localeCompare(b.name))

const rendered = `// This file is generated by scripts/sync-tinted-themes.mjs
// Do not edit by hand.

import type { Theme } from "@/lib/themes"

export const generatedTintedThemes: Theme[] = ${JSON.stringify(
  schemes.map(toTheme),
  null,
  2,
)} as Theme[]
`

await fs.writeFile(outputPath, rendered)
console.log(`Synced ${schemes.length} vendored Tinted themes`)

function parseBase16Yaml(filename, yaml) {
  const lines = yaml.split(/\r?\n/)
  const data = {
    id: filename.replace(/\.(yaml|yml)$/i, ""),
    name: "",
    author: "",
    variant: "",
    palette: {},
  }

  let inPalette = false
  for (const line of lines) {
    if (!line.trim()) continue
    if (line.startsWith("palette:")) {
      inPalette = true
      continue
    }

    if (!inPalette) {
      const match = line.match(/^(\w+):\s*"(.*)"\s*$/)
      if (!match) continue
      const [, key, value] = match
      data[key] = value
      continue
    }

    const paletteMatch = line.match(/^\s+(base[0-9A-F]{2}):\s*"(#[0-9a-fA-F]{6})"/)
    if (paletteMatch) {
      const [, key, value] = paletteMatch
      data.palette[key] = value.toLowerCase()
    }
  }

  if (!data.name || !data.variant) {
    throw new Error(`Could not parse theme metadata for ${filename}`)
  }

  for (const key of [
    "base00",
    "base01",
    "base02",
    "base03",
    "base04",
    "base05",
    "base06",
    "base07",
    "base08",
    "base09",
    "base0A",
    "base0B",
    "base0C",
    "base0D",
    "base0E",
    "base0F",
  ]) {
    if (!data.palette[key]) {
      throw new Error(`Missing ${key} in ${filename}`)
    }
  }

  return data
}

function toTheme(scheme) {
  const p = scheme.palette
  const category = scheme.variant === "light" ? "light" : "dark"
  const surface = category === "dark" ? chooseLowerLuminance([p.base01, p.base02]) : chooseHigherLuminance([p.base01, p.base02])
  const border = category === "dark" ? p.base02 : p.base03
  const primary = pickReadableAccent([p.base0D, p.base0E, p.base0C, p.base0A], p.base00, category)
  const accent = pickReadableAccent([p.base0C, p.base0A, p.base0B, p.base09], p.base00, category, [primary])
  const destructive = pickReadableAccent([p.base08, p.base09, p.base0F], p.base00, category)
  const karaokeActive = pickReadableAccent([p.base0D, p.base0E, p.base0A, p.base0C], p.base00, category)
  const karaokeMuted = pickMuted([p.base03, p.base04], p.base00, category)

  return {
    id: scheme.id,
    name: scheme.name,
    description: `Community Tinted preset · ${summarizeAuthor(scheme.author)}`,
    category,
    tokens: {
      background: p.base00,
      foreground: p.base05,
      card: surface,
      cardForeground: p.base05,
      popover: surface,
      popoverForeground: p.base05,
      primary,
      primaryForeground: bestContrastText(primary, p.base00, p.base07),
      secondary: surface,
      secondaryForeground: p.base05,
      muted: surface,
      mutedForeground: karaokeMuted,
      accent,
      accentForeground: bestContrastText(accent, p.base00, p.base07),
      destructive,
      border,
      input: border,
      ring: primary,
      karaokeActive,
      karaokeMuted,
      karaokeUnsung: withAlpha(karaokeMuted, category === "dark" ? "a6" : "b3"),
      karaokeStageBg: p.base00,
    },
  }
}

function summarizeAuthor(author) {
  if (!author) return "Tinted"
  return author
    .replace(/^https?:\/\//, "")
    .replace(/^www\./, "")
    .split(",")[0]
    .trim()
}

function pickReadableAccent(candidates, background, category, exclude = []) {
  const pool = candidates.filter((candidate) => !exclude.includes(candidate))
  const scored = pool.map((color) => ({
    color,
    contrast: contrastRatio(color, background),
    chroma: saturationScore(color),
  }))

  scored.sort((a, b) => {
    if (Math.abs(a.contrast - b.contrast) > 0.05) return b.contrast - a.contrast
    return b.chroma - a.chroma
  })

  if (category === "light") {
    return scored.find((entry) => entry.contrast >= 3)?.color ?? scored[0].color
  }

  return scored.find((entry) => entry.contrast >= 2.5)?.color ?? scored[0].color
}

function pickMuted(candidates, background, category) {
  const scored = candidates.map((color) => ({
    color,
    contrast: contrastRatio(color, background),
  }))

  if (category === "light") {
    scored.sort((a, b) => b.contrast - a.contrast)
    return scored[0].color
  }

  scored.sort((a, b) => a.contrast - b.contrast)
  return scored.find((entry) => entry.contrast >= 2)?.color ?? scored.at(-1).color
}

function chooseLowerLuminance(colors) {
  return colors.reduce((best, color) =>
    luminance(color) < luminance(best) ? color : best,
  )
}

function chooseHigherLuminance(colors) {
  return colors.reduce((best, color) =>
    luminance(color) > luminance(best) ? color : best,
  )
}

function bestContrastText(background, dark, light) {
  return contrastRatio(background, dark) >= contrastRatio(background, light) ? dark : light
}

function withAlpha(hex, alpha) {
  return `${hex}${alpha}`
}

function saturationScore(hex) {
  const { r, g, b } = hexToRgb(hex)
  return Math.max(r, g, b) - Math.min(r, g, b)
}

function contrastRatio(a, b) {
  const one = luminance(a)
  const two = luminance(b)
  const lighter = Math.max(one, two)
  const darker = Math.min(one, two)
  return (lighter + 0.05) / (darker + 0.05)
}

function luminance(hex) {
  const { r, g, b } = hexToRgb(hex)
  const channels = [r, g, b].map((value) => {
    const normalized = value / 255
    return normalized <= 0.04045
      ? normalized / 12.92
      : ((normalized + 0.055) / 1.055) ** 2.4
  })
  return 0.2126 * channels[0] + 0.7152 * channels[1] + 0.0722 * channels[2]
}

function hexToRgb(hex) {
  const value = hex.replace("#", "")
  return {
    r: Number.parseInt(value.slice(0, 2), 16),
    g: Number.parseInt(value.slice(2, 4), 16),
    b: Number.parseInt(value.slice(4, 6), 16),
  }
}
